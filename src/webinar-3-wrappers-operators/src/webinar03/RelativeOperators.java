public class RelativeOperators {

    public static void main(String[] args) {

        int a = 11, b = 34, k = 16, j = 16;

        System.out.println(a > b);
        System.out.println(b < k);
        System.out.println(b >= k);
        System.out.println(j >= k);
        System.out.println(k == j);
        System.out.println(b != a);



    }
}
